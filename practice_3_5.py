## 탈출 문자. 이스케이프 문자.

# \n : 줄바꿈
print("백문이 불여일견 \n백견이 불여일타")

# \' , \" : 문장 내에서 따옴표
# 저는 "강기범" 입니다.
print("방법 1 저는 '강기범'입니다.")
print('방법 2 저는 "강기범"입니다.')
print("방법 3 저는 \"강기범\"입니다.")
print("방법 3 저는 \'강기범\'입니다.")

# \\ : 문장 내에서 \
print("D:\\PythonWorkspace>")

# \r : 커서를 맨 앞으로 이동
print("Red Apple \rPine")

# \b : 뺵 스페이스 (한 글자 삭제)
print("Redd\bApple")

# \t : tab (4칸 들여쓰기)
print("Red\tApple")